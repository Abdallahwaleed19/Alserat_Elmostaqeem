function normalize(s) {
    return (s || '').trim().toLowerCase().replace(/\s+/g, ' ');
}

function getStaticAnswer(question) {
    const q = normalize(question);
    const qAr = question.trim();
    for (let i = 0; i < ANSWERS.length; i++) {
        if (ANSWERS[i].keys.test(q)) {
            return { answer: ANSWERS[i].ar, sources: ANSWERS[i].sources };
        }
    }
    return null;
}

async function getOpenAIAnswer(question) {
    const apiKey = process.env.OPENAI_API_KEY;
    if (!apiKey || apiKey.trim() === '') {
        console.error('islamic-assistant: OPENAI_API_KEY is not set or empty. Add it in Netlify → Site configuration → Environment variables.');
        return null;
    }
    const systemPrompt = 'أنت مساعد إسلامي. أجب بالعربية فقط. اعتمد في إجابتك على: القرآن الكريم (مع ذكر السورة والآية)، أو السنة الصحيحة (مع ذكر الكتاب إن أمكن، مثل صحيح البخاري/مسلم)، أو قول علماء معتمدين (مع ذكر الاسم أو المصدر). في نهاية كل إجابة اكتب سطراً منفصلاً يبدأ بـ "المصادر:" ثم اذكر المصادر التي استندت إليها (قرآن، حديث، كتاب، عالم). إن لم تكن متأكداً أو المسألة تحتاج فتوى تفصيلية، قل ذلك ووجه المستخدم لدار الإفتاء أو إسلام ويب.';
    try {
        const res = await fetch('https://api.openai.com/v1/chat/completions', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'Authorization': 'Bearer ' + apiKey.trim()
            },
            body: JSON.stringify({
                model: 'gpt-4o-mini',
                messages: [
                    { role: 'system', content: systemPrompt },
                    { role: 'user', content: question }
                ],
                max_tokens: 800,
                temperature: 0.3
            })
        });
        const data = await res.json().catch(() => ({}));
        if (!res.ok) {
            console.error('islamic-assistant: OpenAI API error', res.status, data?.error?.code || '', data?.error?.message || res.statusText);
            return null;
        }
        const text = data?.choices?.[0]?.message?.content || '';
        if (!text) {
            console.error('islamic-assistant: OpenAI returned no content', JSON.stringify(data).slice(0, 200));
            return null;
        }
        const sourcesMatch = text.match(/\n*المصادر?\s*[:\-]\s*([\s\S]+)/i);
        let answer = text;
        let sources = '';
        if (sourcesMatch) {
            answer = text.slice(0, text.indexOf(sourcesMatch[0])).trim();
            sources = sourcesMatch[1].trim();
        } else {
            sources = 'تمت الإجابة بناءً على توجيه المساعد (للتحقق راجع المصادر المعتمدة).';
        }
        return { answer, sources };
    } catch (e) {
        console.error('islamic-assistant: OpenAI request failed', e.message || e);
        return null;
    }
}

exports.handler = async function (event) {
    if (event.httpMethod === 'OPTIONS') {
        return {
            statusCode: 200,
            headers: {
                'Access-Control-Allow-Origin': '*',
                'Access-Control-Allow-Headers': 'Content-Type',
                'Access-Control-Allow-Methods': 'OPTIONS,POST',
            },
            body: '',
        };
    }

    if (event.httpMethod !== 'POST') {
        return {
            statusCode: 405,
            headers: {
                'Content-Type': 'application/json',
                'Access-Control-Allow-Origin': '*',
            },
            body: JSON.stringify({ error: 'Method Not Allowed' }),
        };
    }

    const headers = {
        'Content-Type': 'application/json',
        'Access-Control-Allow-Origin': '*',
    };

    try {
        const body = JSON.parse(event.body || '{}');
        const question = (body.question || '').trim();

        if (!question) {
            return {
                statusCode: 400,
                headers,
                body: JSON.stringify({ error: 'question is required' }),
            };
        }

        // محاولة OpenAI أولاً، ثم الردود الثابتة إذا فشل الاتصال
        let result = await getOpenAIAnswer(question);
        if (!result) {
            result = getStaticAnswer(question);
        }
        if (!result) {
            result = {
                answer: 'تعذّر الاتصال بخدمة المساعد (OpenAI) حالياً. يمكنك الرجوع لدار الإفتاء أو إسلام ويب. للأسئلة عن (الزكاة، الصلاة، الصيام، الصدقة، جمع الصلوات، الوضوء، الحج، أركان الإسلام) جرّب صياغة السؤال بوضوح وسأرد من المراجع الثابتة إذا كان الاتصال يعمل.',
                sources: 'مراجع مقترحة: دار الإفتاء، إسلام ويب، اللجنة الدائمة.'
            };
        }

        return {
            statusCode: 200,
            headers,
            body: JSON.stringify({ answer: result.answer, sources: result.sources || '' }),
        };
    } catch (error) {
        console.error('islamic-assistant error', error);
        return {
            statusCode: 500,
            headers,
            body: JSON.stringify({ error: 'Internal Server Error' }),
        };
    }
};
